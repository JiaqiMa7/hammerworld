def query_matrix(improving_param_id: int, worsening_param_id: int) -> list[int]:
    """Return recommended inventive principle IDs for a contradiction.

    Args:
        improving_param_id: ID of the parameter to improve (1-39)
        worsening_param_id: ID of the parameter that worsens (1-39)

    Returns:
        List of principle IDs (1-40) recommended to resolve the contradiction.
        Returns empty list if no direct mapping exists.
    """
    if not (1 <= improving_param_id <= 39 and 1 <= worsening_param_id <= 39):
        raise ValueError(f"Parameter IDs must be 1-39, got {improving_param_id}, {worsening_param_id}")
    return CONTRADICTION_MATRIX.get((improving_param_id, worsening_param_id), [])


def get_principle_recommendations(improving_name: str, worsening_name: str,
                                  params: dict) -> list[int]:
    """Find principle IDs by parameter names."""
    improving_id = None
    worsening_id = None
    for pid, p in params.items():
        if p.name.lower() == improving_name.lower():
            improving_id = pid
        if p.name.lower() == worsening_name.lower():
            worsening_id = pid
    if improving_id and worsening_id:
        return query_matrix(improving_id, worsening_id)
    return []
